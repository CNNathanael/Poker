    private void jButtonJugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonJugarActionPerformed
        try {
            String NIF = jTextFieldNIF.getText();
            String contrasena = jPasswordField1.getText();
            String contrasena_original = CasaApuestas.jugadores.get(NIF).getContrasena();
            if (CasaApuestas.jugadores.containsKey(NIF)) {
                if (contrasena.equals(contrasena_original)) {
                    VentanaModoJuego jugar = new VentanaModoJuego(this);
                } else {
                    throw new JugadorException(CONTRASENA_INCORRECTA);
                }
            } else {
                throw new JugadorException(NIF_INCORRECTO);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Hay alg√∫n dato incorrecto", "Mensaje", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonJugarActionPerformed

